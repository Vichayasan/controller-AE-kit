def _tiny_text(lcd, text, x, y, color=1, scale=1):
    for ch in text:
        if ch == " ":
            x += 2 * scale
            continue
        glyph = _FONT.get(ch)
        if glyph is None:
            x += 4 * scale
            continue
        for row, pattern in enumerate(glyph):
            for col, pixel in enumerate(pattern):
                if pixel == "1":
                    for dy in range(scale):
                        for dx in range(scale):
                            lcd.pixel(x + col * scale + dx, y + row * scale + dy, color)
        x += 4 * scale


def _draw_left_logo(lcd):
    for page in range(8):
        base = page * LOGO_W
        y0 = page * 8
        for x in range(LOGO_W):
            b = LOGO_LEFT_BITMAP[base + x]
            for bit in range(8):
                if b & (1 << bit):
                    lcd.pixel(x, y0 + bit, 1)


def draw(lcd):
    lcd.fill(0)
    _draw_left_logo(lcd)
    _tiny_text(lcd, "REPCO NEX", TEXT_X, TEXT_Y)
    _tiny_text(lcd, "INDUSTRIAL", TEXT_X, TEXT_Y + TEXT_GAP)
    _tiny_text(lcd, "SOLUTIONS", TEXT_X, TEXT_Y + TEXT_GAP * 2)


def show(lcd, update=None, hold_ms=1200):
    draw(lcd)
    if update is not None:
        update()
    if hold_ms and time is not None:
        time.sleep_ms(hold_ms)
